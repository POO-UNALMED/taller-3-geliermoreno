package taller3.televisores;

public class Marca {

	//atributo
	private String nombre;
	
	//Constructor
	public Marca(String nombre) {
		this.nombre = nombre;
	}
	
	//get's and set's
	
	public String getNombre() {
		return nombre;
	}
	
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
}
